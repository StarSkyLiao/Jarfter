namespace Jarfter.Hexagonal.Direction;

/// <summary>
/// 表示六边形方向在显示朝向下对应的罗盘方向.
/// </summary>
public enum HexagonalCompassDirection
{
    /// <summary>
    /// 正北方向.
    /// </summary>
    North = 0,

    /// <summary>
    /// 东北方向.
    /// </summary>
    NorthEast = 1,

    /// <summary>
    /// 正东方向.
    /// </summary>
    East = 2,

    /// <summary>
    /// 东南方向.
    /// </summary>
    SouthEast = 3,

    /// <summary>
    /// 正南方向.
    /// </summary>
    South = 4,

    /// <summary>
    /// 西南方向.
    /// </summary>
    SouthWest = 5,

    /// <summary>
    /// 正西方向.
    /// </summary>
    West = 6,

    /// <summary>
    /// 西北方向.
    /// </summary>
    NorthWest = 7
}
